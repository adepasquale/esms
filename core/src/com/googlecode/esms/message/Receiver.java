package com.googlecode.esms.message;

/**
 * Class encapsulating receiver's name and number
 * @author Andrea De Pasquale
 */
public class Receiver {
  String name, number;
  
  public Receiver(String number) {
    this(null, number);
  }
  
  public Receiver(String name, String number) {
    this.name = name;
    this.number = number;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getNumber() {
    return number;
  }

  public void setNumber(String number) {
    this.number = number;
  }
}